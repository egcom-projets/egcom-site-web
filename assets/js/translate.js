function translatePage(lang) {
    document.querySelectorAll('[data-translate]').forEach(function(element) {
        var key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    document.querySelectorAll('.flag-btn').forEach(function(btn) {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        }
    });

    localStorage.setItem('language', lang);
}

// Init flag buttons
document.addEventListener('DOMContentLoaded', function() {
    document.querySelectorAll('.flag-btn').forEach(function(btn) {
        btn.addEventListener('click', function() {
            var lang = this.getAttribute('data-lang');
            translatePage(lang);
            document.querySelectorAll('.flag-btn').forEach(function(b) { b.classList.remove('active'); });
            this.classList.add('active');
        });
    });

    document.querySelectorAll('.lang-btn').forEach(function(btn) {
        btn.addEventListener('click', function() {
            var lang = this.getAttribute('data-lang');
            translatePage(lang);
        });
    });

    // Restore saved language
    var savedLang = localStorage.getItem('language');
    if (savedLang && savedLang !== 'fr') {
        translatePage(savedLang);
    }

    // Animate company name
    var container = document.getElementById('companyName');
    if (container) {
        var name = 'EGCOM-PROJETS';
        container.innerHTML = '';
        name.split('').forEach(function(letter, index) {
            var span = document.createElement('span');
            span.textContent = letter;
            span.style.animationDelay = index * 0.1 + 's';
            container.appendChild(span);
        });
    }
});
